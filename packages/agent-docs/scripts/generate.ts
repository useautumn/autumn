import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import config from "../agent-docs.config.js";
import type { Source } from "../src/config/types.js";
import { composeSkill } from "../src/translate/composeSkill.js";
import { composeSources } from "../src/translate/composeSources.js";
import { toMcpResource } from "../src/translate/formats/mcpResource.js";
import { toSkill } from "../src/translate/formats/skill.js";
import type { McpResource, Skill } from "../src/translate/formats/types.js";
import { docsPageToMarkdown } from "../src/translate/ingest/docsPage.js";

// Build-time translation: read canonical docs (+ transitional legacy markdown),
// emit self-contained TS artifacts so consumers need no runtime fs access, plus
// readable rendered .md/SKILL.md for inspection.
const here = dirname(fileURLToPath(import.meta.url));
const docsRoot = resolve(here, "../../../apps/docs/mintlify");
const legacyRoot = resolve(here, "../../mcp/src/resources-v2/concepts");
const contentRoot = resolve(here, "../content");

const readSource = (source: Source): string => {
	if (source.type === "docs") {
		const text = readFileSync(resolve(docsRoot, source.page), "utf8");
		return docsPageToMarkdown({ path: source.page, text });
	}
	return readFileSync(resolve(legacyRoot, source.file), "utf8");
};

// `<docs url="/documentation/..." />` → translated docs page (with its title).
const resolveDocs = (url: string): string => {
	const page = `${url.replace(/^\//, "")}.mdx`;
	return docsPageToMarkdown({
		path: page,
		text: readFileSync(resolve(docsRoot, page), "utf8"),
	});
};

const mcpResources: McpResource[] = [];
const skills: Skill[] = [];

for (const entry of Object.values(config)) {
	if (entry.formats.mcp) {
		const body = composeSources({
			sources: entry.formats.mcp.sources,
			readSource,
		});
		mcpResources.push(
			toMcpResource({
				title: entry.title,
				description: entry.description,
				format: entry.formats.mcp,
				body,
			}),
		);
	}
	if (entry.formats.skill) {
		const { file } = entry.formats.skill;
		const skill = composeSkill({
			path: file,
			text: readFileSync(resolve(contentRoot, file), "utf8"),
			resolveDocs,
		});
		skills.push(toSkill({ skill }));
	}
}

const banner = `// AUTO-GENERATED by scripts/generate.ts — do not edit by hand.
// Canonical source: apps/docs/** and content/**. Run \`bun run gen\` to regenerate.`;

const writeGenerated = ({
	file,
	typeName,
	typeImport,
	value,
}: {
	file: string;
	typeName: string;
	typeImport: string;
	value: unknown;
}) => {
	const outPath = resolve(here, "../src/generated", file);
	mkdirSync(dirname(outPath), { recursive: true });
	writeFileSync(
		outPath,
		`${banner}\n\n${typeImport}\n\nexport const ${typeName} = ${JSON.stringify(value, null, 2)};\n`,
	);
	process.stdout.write(`Wrote ${outPath}\n`);
};

writeGenerated({
	file: "mcp-resources.generated.ts",
	typeName: "mcpResources: McpResource[]",
	typeImport:
		'import type { McpResource } from "../translate/formats/types.js";',
	value: mcpResources,
});
writeGenerated({
	file: "skills.generated.ts",
	typeName: "skills: Skill[]",
	typeImport: 'import type { Skill } from "../translate/formats/types.js";',
	value: skills,
});

// Readable rendered artifacts (human-inspectable; runtime imports the .ts above).
const readableRoot = resolve(here, "../generated");

const writeReadable = ({
	relPath,
	contents,
}: {
	relPath: string;
	contents: string;
}) => {
	const outPath = resolve(readableRoot, relPath);
	mkdirSync(dirname(outPath), { recursive: true });
	writeFileSync(outPath, contents.endsWith("\n") ? contents : `${contents}\n`);
	process.stdout.write(`Wrote ${outPath}\n`);
};

for (const resource of mcpResources) {
	writeReadable({
		relPath: `mcp/${resource.name}.md`,
		contents: resource.text,
	});
}
for (const skill of skills) {
	writeReadable({
		relPath: `skills/${skill.name}/SKILL.md`,
		contents: skill.markdown,
	});
	for (const reference of skill.references) {
		writeReadable({
			relPath: `skills/${skill.name}/${reference.path}`,
			contents: reference.contents,
		});
	}
}
