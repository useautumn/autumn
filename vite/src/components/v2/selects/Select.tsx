"use client";

import * as SelectPrimitive from "@radix-ui/react-select";
import { CheckIcon, ChevronDownIcon, ChevronUpIcon } from "lucide-react";
import * as React from "react";

import { cn } from "@/lib/utils";

const SelectContext = React.createContext<{
	triggerRef: React.RefObject<HTMLButtonElement>;
} | null>(null);

function Select({
	onValueChange,
	onOpenChange,
	...props
}: React.ComponentProps<typeof SelectPrimitive.Root>) {
	const triggerRef = React.useRef<HTMLButtonElement>(null);

	const handleValueChange = (value: string) => {
		onValueChange?.(value);
	};

	const handleOpenChange = (open: boolean) => {
		// When dropdown closes, blur the trigger to remove focus ring
		if (!open) {
			setTimeout(() => {
				if (triggerRef.current) {
					triggerRef.current.blur();
				} else {
					// Fallback: blur the active element if ref isn't set
					(document.activeElement as HTMLElement)?.blur();
				}
			}, 0);
		}
		onOpenChange?.(open);
	};

	return (
		<SelectContext.Provider value={{ triggerRef }}>
			<SelectPrimitive.Root
				data-slot="select"
				onValueChange={handleValueChange}
				onOpenChange={handleOpenChange}
				{...props}
			/>
		</SelectContext.Provider>
	);
}

function SelectGroup({
	...props
}: React.ComponentProps<typeof SelectPrimitive.Group>) {
	return <SelectPrimitive.Group data-slot="select-group" {...props} />;
}

function SelectValue({
	...props
}: React.ComponentProps<typeof SelectPrimitive.Value>) {
	return <SelectPrimitive.Value data-slot="select-value" {...props} />;
}

function SelectTrigger({
	className,
	size = "default",
	children,
	...props
}: React.ComponentProps<typeof SelectPrimitive.Trigger> & {
	size?: "sm" | "default";
}) {
	const context = React.useContext(SelectContext);

	return (
		<SelectPrimitive.Trigger
			ref={context?.triggerRef}
			data-slot="select-trigger"
			data-size={size}
			className={cn(
				// Essential functionality only - no layout-shifting selectors
				"[&_svg:not([class*='text-'])]:text-muted-foreground rounded-lg flex items-center justify-between gap-2 text-sm outline-none disabled:cursor-not-allowed disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4 cursor-pointer",

				// Use only custom styles for visual appearance
				`input-base input-shadow-default input-state-open`,
				className,
			)}
			{...props}
		>
			{children}
			<SelectPrimitive.Icon asChild>
				<ChevronDownIcon className="size-4 opacity-50" />
			</SelectPrimitive.Icon>
		</SelectPrimitive.Trigger>
	);
}

function SelectContent({
	className,
	children,
	position = "popper",
	onEscapeKeyDown,
	...props
}: React.ComponentProps<typeof SelectPrimitive.Content>) {
	return (
		<SelectPrimitive.Portal>
			<SelectPrimitive.Content
				data-slot="select-content"
				onEscapeKeyDown={(e) => {
					// Stop propagation to prevent parent dialogs/sheets from closing
					e.stopPropagation();
					onEscapeKeyDown?.(e);
				}}
				className={cn(
					// z-[200] to appear above sheets (z-[150])
					"bg-interactive-secondary text-popover-foreground relative z-[200] max-h-(--radix-select-content-available-height) min-w-[8rem] overflow-x-hidden overflow-y-auto rounded-md border shadow-md",

					position === "popper" &&
						"data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",

					className,
				)}
				position={position}
				{...props}
			>
				<SelectScrollUpButton />
				<SelectPrimitive.Viewport
					className={cn(
						"p-1",
						position === "popper" &&
							"h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)] scroll-my-1",
					)}
				>
					{children}
				</SelectPrimitive.Viewport>
				<SelectScrollDownButton />
			</SelectPrimitive.Content>
		</SelectPrimitive.Portal>
	);
}

